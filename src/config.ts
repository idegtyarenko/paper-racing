// ── Геометрия стартовой решётки (клетки) ────────────────
// Болиды расставляются рядами от центра трассы к краям, ряд за рядом от линии назад
// (см. layoutStartGrid в track.ts). Ряд заполняется от центра к бокам до START_ROW_MAX
